def particle_layout(nparticle,swarm_x,swarm_z,swarm_rad,swarm_theta,Lx,Lz):

    swarm_mat=np.zeros(nparticle,dtype=np.int32)

    for im in range (0,nparticle):
        if swarm_z[im]<0.2+0.02*np.cos(swarm_x[im]*np.pi/0.9142):
           swarm_mat[im]=1
        else:
           swarm_mat[im]=2

    return swarm_mat

